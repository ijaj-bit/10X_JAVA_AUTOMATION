package FOR_LOOP;

public class LOOP_EVEN_ODD {
    public static void main(String[] args) {
        for(int i=0;i<=15;i++)
        {
            if(i%2==0)
            {
                System.out.println("EVEN->"+i);
            }
            else
            {
                System.out.println("ODD->"+i);
            }
        }
    }
}

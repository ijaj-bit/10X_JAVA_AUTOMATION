package FOR_LOOP;

public class LOOP_7
{
    public static void main(String[] args) {
        for (float f=0.01f;f<5.5f;f++)
        {
            System.out.println("Float is:" + f);
        }
        System.out.println("****************");

        for (long i=0;i<=7;i++)
        {
            System.out.println(i);
        }

        System.out.println("*****************");

        for(int a=0;a>1;a--)
        {
            System.out.println(a);
        }

    }


}

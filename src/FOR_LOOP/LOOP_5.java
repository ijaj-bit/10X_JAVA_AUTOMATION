package FOR_LOOP;

public class LOOP_5 {
    public static void main(String[] args) {
        for (int shruti = 0; shruti < 18; shruti++) {
            if (shruti > 15) {
                System.out.println("Gift from Papa, Iphone");
                break;
            }else{
                System.out.println("No Gift!!");
                break;
            }
        }
    }
}

package DO_WHILE;

public class LOOP_1 {
    public static void main(String[] args) {

        int i=0;
        do{
            System.out.println(i);
            i++;
        }while (i<10);

        System.out.println("______________________________");

        int z=0;
        do {
            System.out.println(z);
            z++;
        }while(z<0);
    }
}
